package model.rec;

public class VideoVO {
	// member field
	int videoNum;
	String videoJanre;
	String videoTitle;	
	String videoDirector;	
	String videoActor;	
	String videoContent;
	String videoRegDate;
	
	public int getVideoNum() {
		return videoNum;
	}
	public void setVideoNum(int videoNum) {
		this.videoNum = videoNum;
	}
	public String getVideoJanre() {
		return videoJanre;
	}
	public void setVideoJanre(String videoJanre) {
		this.videoJanre = videoJanre;
	}
	public String getVideoTitle() {
		return videoTitle;
	}
	public void setVideoTitle(String videoTitle) {
		this.videoTitle = videoTitle;
	}
	public String getVideoDirector() {
		return videoDirector;
	}
	public void setVideoDirector(String videoDirector) {
		this.videoDirector = videoDirector;
	}
	public String getVideoActor() {
		return videoActor;
	}
	public void setVideoActor(String videoActor) {
		this.videoActor = videoActor;
	}
	public String getVideoContent() {
		return videoContent;
	}
	public void setVideoContent(String videoContent) {
		this.videoContent = videoContent;
	}
	public String getVideoRegDate() {
		return videoRegDate;
	}
	public void setVideoRegDate(String videoRegDate) {
		this.videoRegDate = videoRegDate;
	}
	

}
